    //Action Performed used to switch to the help screen from the main screen 
    private void btnHelpBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHelpBookingActionPerformed
        couuntToStartCmboBxAction = 0;
        helpImageTracker++;

        if (helpImageTracker < 4) {
            switch (helpImageTracker) {
                case 0: {
                    readInObj.switchPanels(pnlMainScreen, cardPanel);
                    break;
                }
                case 4: {
                    Image image = readInObj.getHelpImage(0).getScaledInstance(lblHelpImageMS.getWidth(), lblHelpImageMS.getHeight(), Image.SCALE_SMOOTH);
                    lblHelpImageMS.setIcon(new ImageIcon(image));
                    txtAreaHelpMS.setText(readInObj.getHelpText(4));
                    break;
                }
                default: {
                    txtAreaHelpMS.setText(readInObj.getHelpText(helpImageTracker));
                    Image image = readInObj.getHelpImage(helpImageTracker).getScaledInstance(lblHelpImageMS.getWidth(), lblHelpImageMS.getHeight(), Image.SCALE_SMOOTH);
                    lblHelpImageMS.setIcon(new ImageIcon(image));
                    break;
                }
            }

            readInObj.switchPanels(pnlHelp, cardPanel);

        } else {
            helpImageTracker = 0;

            readInObj.switchPanels(pnlMainScreen, cardPanel);
        }

        readInObj.switchPanels(pnlHelp, cardPanel);


    }//GEN-LAST:event_btnHelpBookingActionPerformed

