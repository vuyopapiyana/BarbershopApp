    //Button ActionPerformed for confirming the new account and adding him to the database.
    private void btnSignUpConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpConfirmActionPerformed
        //series of if statements to check whether all fields have values entered.
        boolean allNotNull = false;
        if (!(txtSignUpName.getText().equals(""))) {
            if (!(txtSignUpSurname.getText().equals(""))) {
                if (!(txtSignUpEmail.getText().equals(""))) {
                    if (!(txtSignUpCell.getText().equals(""))) {
                        if (!(txtSignUpUsername.getText().equals(""))) {
                            if (!(pswdSignUpPswd.getText().equals(""))) {
                                if (!(pswdSignUpCPswd.getText().equals(""))) {
                                    allNotNull = true;
                                } else {
                                    JOptionPane.showMessageDialog(null, "You can not leave the confirm password field blank");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "You can not leave the Password field blank\n\nCriteria:\n-Must have at least one capital letter"
                                        + "\n-Must be longer than 7 leters\n-Must have at least one digit");
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "You can not leave the Username field blank");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "You can not leave the Cell Phone field blank");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "You can not leave the Eamil field blank");
                }

            } else {
                JOptionPane.showMessageDialog(null, "You can not leave the Surname field blank");
            }

        } else {
            JOptionPane.showMessageDialog(null, "You can not leave the Name field blank");
        }

        //Series of if statements to check whether the fields are valid
        if (allNotNull) {
            //Checks of the cell phone does not exceed the maximum amount of digits for an int

            String cellString = txtSignUpCell.getText();
            int cellNo = 0;

            if (cellString.length() < 11) {
                cellNo = Integer.parseInt(cellString);

                if (readInObj.nameValidity(txtSignUpName.getText().trim())) {
                    if (readInObj.nameValidity(txtSignUpSurname.getText().trim())) {
                        if (readInObj.emailValidity(txtSignUpEmail.getText().trim())) {
                            if (readInObj.cellValidity(cellNo)) {
                                if (readInObj.usernameValidity(txtSignUpUsername.getText().trim())) {
                                    if (readInObj.passwordValidity(pswdSignUpPswd.getText())) {
                                        if (pswdSignUpCPswd.getText().equals(pswdSignUpPswd.getText())) {
                                            String name = txtSignUpName.getText().trim();
                                            String surname = txtSignUpSurname.getText().trim();
                                            String username = txtSignUpUsername.getText();
                                            String pswd = pswdSignUpPswd.getText();
                                            int cell = Integer.parseInt(txtSignUpCell.getText());
                                            String email = txtSignUpEmail.getText();

                                            //The update SQL statement to be used.
                                            String sql = "Insert into Users (name,surname,username,password,cellPhone,email) values ("
                                                    + "\"" + name + "\" ," + "\"" + surname + "\" ," + "\""
                                                    + username + "\" ," + "\"" + pswd + "\" ," + cell + ", \"" + email + "\"" + ")";

                                            //Adding the new user to the userArr in the ReadInClass
                                            readInObj.setUsers(new User((readInObj.getUserCount() + 1), name, surname, username, pswd, cell, email));
                                            readInObj.setSignedInUser(new User((readInObj.getUserCount()), name, surname, username, pswd, cell, email));
                                            readInObj.runUpdateQuery(sql);
                                            JOptionPane.showMessageDialog(null, "You have been successfully added to the databse");
                                            readInObj.switchPanels(pnlMainScreen, cardPanel);
                                            loggedIn = true;
                                            lblMainScreenUsername.setText(username);
                                            lblProfileNAnswer.setText(name);
                                            lblProfileSAnswer.setText(surname);
                                            lblProfileEAnswer.setText(email);
                                            lblProfileCAnswer.setText("0" + cell);
                                            lblUsernameLogOut.setText("Signed In: " + username);

                                        } else {
                                            JOptionPane.showMessageDialog(null, "Passwords do not match");
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "Password is invalid\n\nCriteria:\n-Must have at least one capital letter"
                                                + "\n-Must be longer than 7 leters\n-Must have at least one digit");
                                    }

                                }// Username validity. Error message is within validity check.

                            }// Cell validity. Error message is within validity check.

                        } else {
                            JOptionPane.showMessageDialog(null, "Email is invalid");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Surname is invalid. Please do not put anything other than letters in your name");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Name is invalid. Please do not put anything other than letters in your name");
                }

            } else {
                JOptionPane.showMessageDialog(null, "The number is too long");
            }
        }

    }//GEN-LAST:event_btnSignUpConfirmActionPerformed

    //ActionPerformed for when user presses next on the help screen.
    //Fetches and displays the next text message and image.
    private void btnHelpNextMSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHelpNextMSActionPerformed
        //Increments index of images and text when user presses next
        helpImageTracker++;
        //Checks if the helpImageTracker is within the index of images.
        //There are only four images. If the tracker is above 4 then the screen sets the tracker to the first index and switches to the 
        //main screen.
        if (helpImageTracker < 5) {
            //Because of the order the images are stores in the imageList array of getHelpImage(int index) when the tracker is 4
            //the program retrieves the image at position 0. Else if the tracker is not equal to 4 then the tracker is sent
            //as the index when retreiving the image and text.
            if (helpImageTracker == 4) {
                
                Image image = readInObj.getHelpImage(0).getScaledInstance(lblHelpImageMS.getWidth(), lblHelpImageMS.getHeight(), Image.SCALE_SMOOTH);
                lblHelpImageMS.setIcon(new ImageIcon(image));
                txtAreaHelpMS.setText(readInObj.getHelpText(helpImageTracker)); 
            } else {
                txtAreaHelpMS.setText(readInObj.getHelpText(helpImageTracker));
                Image image = readInObj.getHelpImage(helpImageTracker).getScaledInstance(lblHelpImageMS.getWidth(), lblHelpImageMS.getHeight(), Image.SCALE_SMOOTH);
                lblHelpImageMS.setIcon(new ImageIcon(image));

            }
        } else {
            helpImageTracker = 1;
            txtAreaHelpMS.setText(readInObj.getHelpText(helpImageTracker));
            Image image = readInObj.getHelpImage(helpImageTracker).getScaledInstance(lblHelpImageMS.getWidth(), lblHelpImageMS.getHeight(), Image.SCALE_SMOOTH);
            lblHelpImageMS.setIcon(new ImageIcon(image));
            readInObj.switchPanels(pnlMainScreen, cardPanel);
        }


    }//GEN-LAST:event_btnHelpNextMSActionPerformed

