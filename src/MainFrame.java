    //ActionPerformed to add the booking to the database.
    private void btnNextBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextBookingActionPerformed

        double amount = Double.parseDouble(lblCostBookingA.getText());
        double paid = spinnerPaid.getValue();
        //If Statement to check if the user has paid enough money for the hairuct.
        if (paid  - amount >0) { 
            if(paid - amount <=400){
            double change = (paid - amount) * 100;
            change = Math.round(change);
            change = change / 100;

            lblChangeBooking.setText("Change: R " + change);

            //Code to extract the date and format it as "dd-MM-yyyy". If this is not ddone, the program will include time
            // which will make two dates of the same date not be equal.
            //e.g -09-06-2016 will not be equal to 09-06-2016
            Date selected_date = jDate.getDate();
            SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
            try {
                selected_date = format.parse(format.format(selected_date));
            } catch (ParseException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            //If statement to determine whether the booking selected is valid or not 
            if (readInObj.confirmBookingDate(jDate.getDate())) {

                //If statement to call method which will create the booking with the entered details
                if (readInObj.createBRef((String) cmboBxBarber.getSelectedItem(), jDate.getDate(), (String) cmboBxHaircut.getSelectedItem(), cmboBxBarber.getSelectedIndex())) {
                    //Booking has been successfully added. Incrementing booking counter to indicate user has a booking.
                    countBooking++;
                    JOptionPane.showMessageDialog(null, "Booking was successfully added! ");
                    readInObj.switchPanels(pnlMainScreen, cardPanel);
                    couuntToStartCmboBxAction = 0;
                }

            }
            }else{
                JOptionPane.showMessageDialog(null, "You can not pay over R400 more than the haircut cost");
            }
        } else {
            //Else displays the amount of money left for the user to pay
            double change = (amount - paid) * 100;
            change = Math.round(change);
            change = change / 100;
            JOptionPane.showMessageDialog(null, "Insufficient amount paid. Please pay R " + change + " more.");
        }

    }//GEN-LAST:event_btnNextBookingActionPerformed

