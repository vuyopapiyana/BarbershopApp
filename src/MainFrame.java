    //Method to confirm the updated fields entered in the edit profile screen and change them in the database and user array.
    private void btnEPConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEPConfirmActionPerformed
        String name = txtEPName.getText();
        String surname = txtEPSurname.getText();
        String email = txtEPEmail.getText();
        String username = txtEPUsername.getText();
        String cPswd = pswdEPConfirmP.getText();
        String password = pswdEPPassword.getText();
        String cellString = txtEPCell.getText();
        boolean validCell = true;
        int cell = 0;

        //Series of if statements to check if the field is empty
        //When field is empty, the variable is initialised to the old values
        if (name.isEmpty()) {
            name = readInObj.getSignedInUser().getName();
        }
        if (surname.isEmpty()) {
            surname = readInObj.getSignedInUser().getSurname();
        }
        if (email.isEmpty()) {
            email = readInObj.getSignedInUser().getEmail();
        }
        if (username.isEmpty()) {
            username = readInObj.getSignedInUser().getUsername();
        }
        if (password.isEmpty()) {
            password = readInObj.getSignedInUser().getPassword();
            cPswd = password;
        }
        if (cellString.isEmpty()) {
            cell = readInObj.getSignedInUser().getCell();
            String c = "0" + cell;
            cell = Integer.parseInt(c);
        } else {
            cell = Integer.parseInt(cellString);

            if (!(readInObj.cellValidity(cell))) {
                validCell = false;
            } else {
                cell = Integer.parseInt(txtEPCell.getText());
            }

        }

        //Series of if statements to check the valid of all fields
        if (readInObj.nameValidity(name)) {
            if (readInObj.nameValidity(surname)) {
                if (readInObj.emailValidity(email)) {
                    if (validCell) {
                        if (readInObj.passwordValidity(password)) {
                            if (!cPswd.isEmpty()) {
                                if (password.equals(cPswd)) {
                                    //All fields are valid. The insert into query is run and the new values are sent to the database.
                                    //Below asks the user if he is sure he wants to update the fields. If user says 'yes', the fields will be updates
                                    //but if user says 'no' the fields are not updated.
                                    int decision = JOptionPane.showConfirmDialog(null, "Are you sure you want to update your profile?"
                                            + "\nAny empty fields are kept to their original values.", "Appointment", JOptionPane.YES_NO_OPTION);
                                    if (decision == 0) {
                                        txtEPName.setText("");
                                        txtEPSurname.setText("");
                                        txtEPEmail.setText("");
                                        txtEPUsername.setText("");
                                        txtEPCell.setText("");
                                        pswdEPPassword.setText("");
                                        pswdEPConfirmP.setText("");
                                        lblProfileNAnswer.setText(name);
                                        lblProfileSAnswer.setText(surname);
                                        lblProfileEAnswer.setText(email);
                                        lblProfileCAnswer.setText(cell + "");
                                        lblMainScreenUsername.setText(username);
                                        lblUsernameLogOut.setText("Signed In: " + username);
                                        readInObj.setAllUserFields(name, surname, email, username, password, cell);
                                        readInObj.switchPanels(pnlViewProfile, cardPanel);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(this, "Passwords do not match", "Password Error", JOptionPane.ERROR_MESSAGE);
                                }
                            } else {
                                JOptionPane.showMessageDialog(this, "You may not leave the password field empty", "Password Error", JOptionPane.ERROR_MESSAGE);
                            }

                        } else {
                            JOptionPane.showMessageDialog(this, "Password does not meet security requirements", "Password Error", JOptionPane.ERROR_MESSAGE);
                        }

                    }//Else for the cell validity. Error message is displayed from the validity method

                } else {
                    JOptionPane.showMessageDialog(this, "Email is not valid ", "Email Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(this, "User is not allowed any digits or"
                        + " special characters in surname", "Surname Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "User is not allowed any digits or"
                    + " special characters in name", "Name Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEPConfirmActionPerformed

